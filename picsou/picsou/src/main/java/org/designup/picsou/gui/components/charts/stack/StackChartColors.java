package org.designup.picsou.gui.components.charts.stack;

import java.awt.*;

public class StackChartColors {
  private Color barColor = Color.red.brighter();
  private Color barTextColor = Color.white;
  private Color labelColor = Color.gray;

  public Color getBarColor() {
    return barColor;
  }

  public Color getBarTextColor() {
    return barTextColor;
  }

  public Color getLabelColor() {
    return labelColor;
  }
}
